package chapter12.example2;

public class Fruits {
	private int price;
	private int amount;
	
	public Fruits(int price, int amount) {
		this.price = price;
		this.amount = amount;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	
}
